def multiply_nums(num1, num2):
    """
    Multiply two numbers.

    :param num1: The first factor.
    :type num1: float or int
    :param num2: The second factor.
    :type num2: float or int
    :return: The result of the multiplication.
    :rtype: float or int
    """
    answer = num1 * num2
    return answer
