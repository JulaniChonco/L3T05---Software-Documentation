def subtract_nums(num1, num2):
    """
    Subtract one number from another.

    :param num1: The minuend.
    :type num1: float or int
    :param num2: The subtrahend.
    :type num2: float or int
    :return: The result of the subtraction.
    :rtype: float or int
    """
    answer = num2 - num1
    return answer
